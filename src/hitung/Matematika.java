package hitung;

// Class Matematika
public class Matematika {
    // Attributes
    double bil1;
    double bil2;
    
    // Constructor
    public Matematika(double bil1, double bil2) {
        this.bil1 = bil1;
        this.bil2 = bil2;
    }
    
    // Method setPenjumlahan
    public double setPenjumlahan() {
        return bil1 + bil2;
    }
}
